export const banner = new Command()
	.setManual({
		purpose: "Display a message in large letters",
		usage: "banner [STRING]...",
		description: "Prints the ASCII art representation of the provided text.",
	})
	.setExecute(function(this: Command, args, { stdout }) {
		if (!args.length)
			return EXIT_CODE.success;

		const input = args.join(" ").toUpperCase();
		const rows = ["", "", "", "", ""];

		for (const char of input) {
			const glyph = FONT_MAP[char] ?? FONT_MAP["?"];
			for (let i = 0; i < 5; i++) {
				rows[i] += glyph[i] + "  ";
			}
		}

		Shell.printLn(stdout, rows.join("\n"));
		return EXIT_CODE.success;
	});